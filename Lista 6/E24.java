public class E24 {
	
	public static void main (String[] args) {
		
		int num;
		
		for(int i = 1; i <= 10; i++){
			
			num = i;
			
			for(int x = 0; x <= 10; x++){
				
				System.out.printf("\n%d X %d = %d", num, x, (num * x));
				
				
			}
			System.out.println("\n");
			
		}
		
		
	}
}

